var product_list_steps = function () {

	this.Then(/^I should see my ISA available in the list$/, function (screen, callback) {
	    callback.pending();
	  });

	this.When(/^I choose to view the More Menu$/, function (screen, callback) {
	    callback.pending();
	  });

	this.Then(/^I should be presented with all my available Products$/, function (screen, callback) {
	    callback.pending();
	  });

	this.When(/^I open each of my available plans$/, function (screen, callback) {
	    callback.pending();
	  });

	this.Then(/^I should be presented with the correct data for that plan$/, function (screen, callback) {
	    callback.pending();
	  });

	this.Then(/^I should be presented with S&S ISA advert$/, function (screen, callback) {
	    callback.pending();
	  });

	this.When(/^I open the S&S ISA advert$/, function (screen, callback) {
	    callback.pending();
	  });

	this.Then(/^I should see the S&S ISA page$/, function (screen, callback) {
	    callback.pending();
	  });

	this.Then(/^I Should see the correct remaining days left to apply for an ISA$/, function (screen, callback) {
	    callback.pending();
	  });

};

module.exports = product_list_steps;